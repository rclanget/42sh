int			ft_isalnum_or_dot(int c)
{
	if ((c >= '0' && c <= '9') || (c >= 'a' && c <= 'z') || c == '.' ||\
	c == '-' || c == '_' || c == '+')
		return (1);
	if ((c >= 'A' && c <= 'Z'))
		return (1);
	else
		return (0);
}
