#include "libft.h"

int		ft_str_isalnum_spe(char *s)
{
	int i;

	i = 0;
	while (s[i])
	{
		if (!(ft_isalnum(s[i])) && s[i] != '=')
			return (0);
		i++;
	}
	return (1);
}
